    // 会員登録
    public function register(RegisterRequest $request) {
        $user = User::create([
            'name' =>  $request->name,
            'email' => $request->email,
            'password' => Hash::make($request->password),
        ]);

        // 作成されたユーザーに新しい個人アクセストークンを生成
        $token = $user->createToken('auth_token')->plainTextToken;

        return response()->json(['access_token' => $token,
        'token_type' => 'Bearer',]);
    }

